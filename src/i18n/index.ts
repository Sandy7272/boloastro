export const initializeLanguage = () => {
  if (typeof window !== 'undefined') {
    const saved = localStorage.getItem('boloastro_language');
    if (saved && ['en', 'hi', 'mr'].includes(saved)) {
      i18n.changeLanguage(saved);
    }
  }
};

